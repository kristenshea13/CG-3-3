using System;

namespace CG_3_3
{
    class Program
    {
        static void Main(string[] args)
        {
            //statement - complete "sentence" of code, made up of expression(s)
            Console.WriteLine("Good Morning!");
            Console.ReadLine();

            //expression - uses operators and operands
            int b = 2;
            //operand

            int c = 1;
            //operand

            int d = b - c;
            //expression

            Console.WriteLine(d);
            Console.ReadLine();


            //operator - used for completing action within the code
            int x = 1;
            int y = 2;
            Console.WriteLine(x + y);
            Console.ReadLine();
            //operators used = + ( ) ; .


            //operand - values (need defining)
            Console.Write("What is your name? ");
            string myName = Console.ReadLine();
            //"myName" is the operand






        }
    }
}
